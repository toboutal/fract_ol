#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t i;
	size_t j;
	size_t k;

	i = 0;
	j = 0;
	k = size > ft_strlen(dst) ? ft_strlen(dst) : size;
	while (dst[i])
		i++;
	if (size == 0)
		return (i);
	while (src[j] && i < size - 1)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = 0;
	return (k + ft_strlen(src));
}
