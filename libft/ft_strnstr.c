#include "libft.h"

char	*ft_strnstr(const char *haystack, const char *needle, size_t len)
{
	size_t i;
	size_t j;

	i = 0;
	if (ft_strcmp(needle, "") == 0)
		return ((char *)haystack);
	while (haystack[i] && i < len)
	{
		j = 0;
		while (needle[j] == haystack[i] && i < len)
		{
			j++;
			i++;
			if (needle[j] == 0)
				return ((char *)haystack + (i - j));
		}
		i = i - j;
		i++;
	}
	return (NULL);
}
